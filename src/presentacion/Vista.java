    public void llenarMuestra(ArrayList<Integer> susp) {
        int valor = 0;
        int i = 0;
        listMuestra.removeAll();
        while (susp.size() > i) {
            valor = susp.get(i);
            listMuestra.add(Integer.toString(valor));
            i++;
        }
    }

